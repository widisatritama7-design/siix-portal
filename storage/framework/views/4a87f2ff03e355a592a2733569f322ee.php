        // Sound effects
        function playCoinSound() {
            try {
                const AudioContext = window.AudioContext || window.webkitAudioContext;
                const ctx = new AudioContext();
                
                // Resume if suspended
                if (ctx.state === 'suspended') {
                    ctx.resume();
                }
                
                const now = ctx.currentTime;
                const masterGain = ctx.createGain();
                masterGain.gain.value = 0.5;
                masterGain.connect(ctx.destination);
                
                // Suara uang koin - nada tinggi turun cepat seperti "cling" suara koin jatuh
                const frequencies = [1200, 800, 600, 400];
                const duration = 0.05;
                
                frequencies.forEach((freq, i) => {
                    const osc = ctx.createOscillator();
                    const gain = ctx.createGain();
                    osc.connect(gain);
                    gain.connect(masterGain);
                    osc.frequency.value = freq;
                    osc.type = 'sine';
                    gain.gain.value = 0.3;
                    osc.start(now + (i * duration));
                    gain.gain.exponentialRampToValueAtTime(0.00001, now + (i * duration) + duration);
                    osc.stop(now + (i * duration) + duration);
                });
                
                // Tambahan suara "cling" di akhir
                setTimeout(() => {
                    const osc2 = ctx.createOscillator();
                    const gain2 = ctx.createGain();
                    osc2.connect(gain2);
                    gain2.connect(masterGain);
                    osc2.frequency.value = 800;
                    osc2.type = 'sine';
                    gain2.gain.value = 0.2;
                    osc2.start();
                    gain2.gain.exponentialRampToValueAtTime(0.00001, ctx.currentTime + 0.1);
                    osc2.stop(ctx.currentTime + 0.1);
                }, 200);
                
                setTimeout(() => {
                    masterGain.disconnect();
                }, 500);
                
            } catch(e) {
                console.log('Audio not supported:', e);
